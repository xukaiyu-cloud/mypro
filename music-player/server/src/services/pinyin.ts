export function toPinyinInitials(text: string): string {
  let result = ''
  for (const ch of text) result += PINYIN_MAP.get(ch) || ch.toLowerCase()
  return result
}

export function isPinyinPattern(input: string): boolean {
  return /^[a-zA-Z]+$/.test(input) && input.length <= 10
}

const ALIAS_MAP: Record<string, string[]> = {
  '周杰伦': ['周董','jay','杰伦'],
  '林俊杰': ['jj','俊杰'],
  '陈奕迅': ['eason','医生'],
  '五月天': ['mayday','5月天'],
  '邓紫棋': ['gem','紫棋'],
  '薛之谦': ['薛薛','老薛'],
}

export function expandAliases(keyword: string): string[] {
  const terms = [keyword]
  for (const [name, aliases] of Object.entries(ALIAS_MAP)) {
    if (aliases.some(a => a.toLowerCase() === keyword.toLowerCase())) terms.push(name)
    else if (name.includes(keyword)) terms.push(...aliases)
  }
  return [...new Set(terms)]
}
